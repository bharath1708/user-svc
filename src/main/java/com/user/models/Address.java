package com.user.models;

public class Address {

	private String streetNameFirst;
	private String streetNameSecond;
	private String area;
	private String city;
	/**
	 * @return the streetNameFirst
	 */
	public String getStreetNameFirst() {
		return streetNameFirst;
	}
	/**
	 * @param streetNameFirst the streetNameFirst to set
	 */
	public void setStreetNameFirst(String streetNameFirst) {
		this.streetNameFirst = streetNameFirst;
	}
	/**
	 * @return the streetNameSecond
	 */
	public String getStreetNameSecond() {
		return streetNameSecond;
	}
	/**
	 * @param streetNameSecond the streetNameSecond to set
	 */
	public void setStreetNameSecond(String streetNameSecond) {
		this.streetNameSecond = streetNameSecond;
	}
	/**
	 * @return the area
	 */
	public String getArea() {
		return area;
	}
	/**
	 * @param area the area to set
	 */
	public void setArea(String area) {
		this.area = area;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	
	
	
	
}
